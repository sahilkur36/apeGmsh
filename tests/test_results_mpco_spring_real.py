class TestSingleSpringElement:
    def test_spring_force_bare_canonical_includes_both_buckets(
        self, results,
    ) -> None:
        # Bare ``spring_force`` parses to (root, 0) and so it pulls
        # column 0 of every bucket — the only column of the 1-spring
        # bucket (elem 100) plus the first column of the 3-spring
        # bucket (elem 200's spring 0). Both elements appear.
        slab = results.elements.springs.get(component="spring_force")
        np.testing.assert_array_equal(
            np.sort(slab.element_index), [100, 200],
        )
        assert slab.values.shape == (5, 2)

    def test_elem_100_force_matches_closed_form(self, results) -> None:
        # Pattern: ``load 2 200 0 0``; LoadControl dLambda=0.2; k=1000.
        # F_x(elem 100) = 200 * lambda → step k => 40 * (k+1).
        slab = results.elements.springs.get(component="spring_force")
        col = int(np.where(slab.element_index == 100)[0][0])
        expected = 40.0 * np.arange(1, 6)
        np.testing.assert_array_almost_equal(slab.values[:, col], expected)

    def test_elem_100_deformation_matches_closed_form(
        self, results,
    ) -> None:
        # u = F / k = 0.04 * (k+1).
        slab = results.elements.springs.get(component="spring_deformation")
        col = int(np.where(slab.element_index == 100)[0][0])
        expected = 0.04 * np.arange(1, 6)
        np.testing.assert_array_almost_equal(slab.values[:, col], expected)

    def test_out_of_range_spring_index_is_empty_or_partial(
        self, results,
    ) -> None:
        # spring_force_1 is out of range for the 1-spring bucket
        # (skipped) but in range for the 3-spring bucket → only
        # elem 200 remains.
        slab = results.elements.springs.get(component="spring_force_1")
        np.testing.assert_array_equal(slab.element_index, [200])

        # spring_force_3 is out of range for both buckets → empty slab.
        slab3 = results.elements.springs.get(component="spring_force_3")
        assert slab3.values.shape == (5, 0)


