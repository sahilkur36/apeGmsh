class TestRawMpcoLayoutMatchesAssumptions:
    """Independent of the reader — sanity-check the on-disk shape so
    the test file documents what the reader is targeting."""

    def test_basicForce_buckets_are_split_by_n_springs(
        self, mpco_path: Path,
    ) -> None:
        with h5py.File(mpco_path, "r") as f:
            on_elem = f["MODEL_STAGE[1]/RESULTS/ON_ELEMENTS"]
            assert "basicForce" in on_elem  # per-spring force lives here
            bf = on_elem["basicForce"]
            keys = sorted(bf.keys())
            assert keys == ["19-ZeroLength[1:0:0]", "19-ZeroLength[1:0:1]"]
            # 1-spring bucket: NUM_COMPONENTS=1, holds elem 100.
            b1 = bf["19-ZeroLength[1:0:0]"]
            assert int(b1["META/NUM_COMPONENTS"][0, 0]) == 1
            assert b1["ID"][...].flatten().tolist() == [100]
            # 3-spring bucket: NUM_COMPONENTS=3, holds elem 200.
            b3 = bf["19-ZeroLength[1:0:1]"]
            assert int(b3["META/NUM_COMPONENTS"][0, 0]) == 3
            assert b3["ID"][...].flatten().tolist() == [200]

    def test_deformation_buckets_are_split_by_n_springs(
        self, mpco_path: Path,
    ) -> None:
        with h5py.File(mpco_path, "r") as f:
            on_elem = f["MODEL_STAGE[1]/RESULTS/ON_ELEMENTS"]
            assert "deformation" in on_elem
            d = on_elem["deformation"]
            assert sorted(d.keys()) == [
                "19-ZeroLength[1:0:0]", "19-ZeroLength[1:0:1]",
            ]
            assert int(d["19-ZeroLength[1:0:0]/META/NUM_COMPONENTS"][0, 0]) == 1
            assert int(d["19-ZeroLength[1:0:1]/META/NUM_COMPONENTS"][0, 0]) == 3

    def test_data_step_shape_matches_n_components(
        self, mpco_path: Path,
    ) -> None:
        with h5py.File(mpco_path, "r") as f:
            d3 = f[
                "MODEL_STAGE[1]/RESULTS/ON_ELEMENTS/basicForce/"
                "19-ZeroLength[1:0:1]/DATA/STEP_4"
            ]
            assert d3.shape == (1, 3)  # 1 elem, 3 springs


