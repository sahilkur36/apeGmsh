"""Phase 11d — real MPCO read of ZeroLength spring buckets.

Drives ``Results.from_mpco(...)`` against a small live MPCO fixture
generated by an actual STKO/OpenSees run (Tcl script committed
alongside at ``tests/fixtures/results/gen_zl_springs.tcl``). The
fixture contains two ZeroLength elements that exercise both the
1-spring and the multi-spring (N=3) header_idx buckets:

- elem 100: ``zeroLength`` with ``-dir 1 -mat 1`` (1 spring)
  → bucket ``19-ZeroLength[1:0:0]`` with ``NUM_COMPONENTS=1``.
- elem 200: ``zeroLength`` with ``-dir 1 2 3 -mat 1 1 1`` (3 springs)
  → bucket ``19-ZeroLength[1:0:1]`` with ``NUM_COMPONENTS=3``.

A ramped force pattern (5 LoadControl steps, dLambda=0.2, k=1000 per
spring) drives a known per-step force / deformation, so the slab
values can be checked against closed-form expectations.

Validates:

1. ``available_components`` surfaces ``spring_force`` (1-spring root)
   plus ``spring_force_0`` … ``spring_force_2`` (3-spring indexed)
   and the matching deformation canonicals.
2. Slab values from the public ``elements.springs.get(...)`` API match
   the raw bucket DATA columns exactly.
3. The 1-spring element receives only the bare ``spring_force``
   canonical (length-1 row), and out-of-range spring indices on the
   1-spring element return an empty slab.
4. Force / deformation are linked by ``F = k * e`` with ``k=1000``,
   confirming the per-spring routing (``basicForce`` for force,
   ``deformation`` for deformation) is correct end-to-end.
"""
from __future__ import annotations

from pathlib import Path

import h5py
import numpy as np
import pytest

from apeGmsh.results import Results
from apeGmsh.results.readers._protocol import ResultLevel


_FIXTURE = Path(__file__).parent / "fixtures" / "results" / "zl_springs.mpco"


