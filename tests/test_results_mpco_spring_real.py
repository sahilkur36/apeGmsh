class TestFilters:
    def test_element_id_filter_picks_only_3_spring_element(
        self, results,
    ) -> None:
        slab = results.elements.springs.get(
            component="spring_force_0", ids=[200],
        )
        np.testing.assert_array_equal(slab.element_index, [200])
        assert slab.values.shape == (5, 1)

    def test_time_filter_single_step(self, results) -> None:
        slab = results.elements.springs.get(
            component="spring_force_0", time=4,
        )
        # One step, both elements (since spring 0 exists in both buckets)
        assert slab.values.shape[0] == 1
