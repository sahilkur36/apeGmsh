class TestMultiSpringElement:
    def test_spring_force_0_value_matches_raw_bucket(
        self, results, mpco_path: Path,
    ) -> None:
        slab = results.elements.springs.get(component="spring_force_0")
        # element 200's spring 0 (x dir): F = 100 * lambda = 20*(k+1).
        # Slab includes elem 200 only (1-spring bucket skipped — index
        # 0 IS in range there, so it shows elem 100 too. Verify both.)
        # spring_force_0 hits BOTH buckets:
        #  - 1-spring bucket (col 0 = the only column)  → elem 100 force
        #  - 3-spring bucket (col 0)                     → elem 200 spring 0
        ids = sorted(slab.element_index.tolist())
        assert ids == [100, 200]
        # Verify against raw DATA at the last step.
        with h5py.File(mpco_path, "r") as f:
            raw_b1 = f[
                "MODEL_STAGE[1]/RESULTS/ON_ELEMENTS/basicForce/"
                "19-ZeroLength[1:0:0]/DATA/STEP_4"
            ][...]
            raw_b3 = f[
                "MODEL_STAGE[1]/RESULTS/ON_ELEMENTS/basicForce/"
                "19-ZeroLength[1:0:1]/DATA/STEP_4"
            ][...]
        for eid, expected_val in (
            (100, float(raw_b1[0, 0])),
            (200, float(raw_b3[0, 0])),
        ):
            col = np.where(slab.element_index == eid)[0][0]
            assert slab.values[-1, col] == pytest.approx(expected_val)

    def test_spring_force_2_value_matches_raw_bucket(
        self, results, mpco_path: Path,
    ) -> None:
        # spring_force_2 only exists for the 3-spring bucket (elem 200).
        slab = results.elements.springs.get(component="spring_force_2")
        np.testing.assert_array_equal(slab.element_index, [200])
        # Force on spring 2 of elem 200 (z dir): load 4 ... 25 →
        # F_z = 25 * lambda; step k => 5 * (k+1).
        expected = (5.0 * np.arange(1, 6)).reshape(5, 1)
        np.testing.assert_array_almost_equal(slab.values, expected)
        # Cross-check vs raw bucket col 2.
        with h5py.File(mpco_path, "r") as f:
            raw_b3 = f[
                "MODEL_STAGE[1]/RESULTS/ON_ELEMENTS/basicForce/"
                "19-ZeroLength[1:0:1]/DATA/STEP_4"
            ][...]
        assert slab.values[-1, 0] == pytest.approx(float(raw_b3[0, 2]))

    def test_force_equals_stiffness_times_deformation(self, results) -> None:
        # k = 1000 for every spring → F == 1000 * e for all 3 springs.
        for idx in (0, 1, 2):
            f_slab = results.elements.springs.get(
                component=f"spring_force_{idx}",
            )
            d_slab = results.elements.springs.get(
                component=f"spring_deformation_{idx}",
            )
            # Filter both slabs down to elem 200 (the 3-spring element).
            f_col = np.where(f_slab.element_index == 200)[0][0]
            d_col = np.where(d_slab.element_index == 200)[0][0]
            np.testing.assert_array_almost_equal(
                f_slab.values[:, f_col],
                1000.0 * d_slab.values[:, d_col],
            )


